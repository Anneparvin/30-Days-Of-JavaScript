// Arithmetic operators are mathematical operators.

// Addition(+): a + b
// Subtraction(-): a - b
// Multiplication(*): a * b
// Division(/): a / b
// Modulus(%): a % b
// Exponential(**): a ** b

let a = 4;
let b = 3;

let total = a + b;
let difference = a - b;
let product = a * b;
let quotient = a / b;
let modulo = a % b;
let exponent = a ** b;

console.log(total, difference, product, quotient, modulo, exponent); // 7,1,12,1.33,1,64
